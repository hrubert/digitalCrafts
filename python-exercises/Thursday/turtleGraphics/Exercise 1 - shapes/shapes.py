import turtle

t = turtle.Turtle()
t.shape("turtle")

def circle():
    t.penup()
    t.goto(-200, 160)
    t.pendown()
    t.circle(70)

def triangle():
    t.penup()
    t.goto(0, 150)
    t.pendown()
    t.left(120)
    t.forward(120)
    t.left(120)
    t.forward(120)
    t.left(120)
    t.forward(120)

